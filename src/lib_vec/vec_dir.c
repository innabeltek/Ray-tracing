#include "vector.h"

t_vector	vec_dir(t_vector *v1, t_vector *v2, t_vector *res)
{
	res->x = v2->x - v1->x;
	res->y = v2->y - v1->y;
	res->z = v2->z - v1->z;
	return (*res);
}
